/**
 * 문제 설명:
 * OO 연구소는 한 번에 K 칸을 앞으로 점프하거나, (현재까지 온 거리) x 2에 해당하는 위치로 순간이동할 수 있는 아이언 슈트를 개발했습니다.
 * 주어진 거리 N 만큼 떨어진 장소로 가기 위해 필요한 최소 건전지 사용량을 반환하는 함수를 작성하세요.
 *
 * 제한 사항:
 * 1 <= N <= 10^9
 *
 * 입출력 예:
 * N = 5 -> return 2
 * N = 6 -> return 2
 * N = 5000 -> return 5
 */

function solution(n){
    var ans = 0;
    while(n !== 0){
        if(n % 2 === 0){
            n >>= 1;
        }else{
            n -= 1;
            ans++;
        }
    }
    return ans;
}

// 입출력 예시 테스트
console.log(solution(5));    // 기대 값: 2
console.log(solution(6));    // 기대 값: 2
console.log(solution(5000)); // 기대 값: 5

/**
 * 시간 복잡도:
 * O(log N): N을 절반으로 줄이는 과정을 반복하므로 로그 복잡도를 가집니다.
 *
 * 공간 복잡도:
 * O(1): 추가적인 공간을 사용하지 않으므로 상수 공간입니다.
 */

/**
 * 개선할 점:
 * - 현재 알고리즘은 이미 최적화된 형태이며, O(log N)의 시간 복잡도를 유지하고 있습니다.
 * - 알고리즘의 명확성을 높이기 위해 코드의 가독성을 개선할 수 있습니다.
 */